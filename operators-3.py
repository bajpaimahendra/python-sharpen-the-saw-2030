################## Comparison Operators (Most Used) #############################################
#           
#        | Operator | Meaning          |
#        | -------- | ---------------- |
#        | `==`     | equal            |
#        | `!=`     | not equal        |
#        | `>`      | greater          |
#        | `<`      | smaller          |
#        | `>=`     | greater or equal |
#        | `<=`     | smaller or equal |
#
################# Logical Operators (Combine Conditions) ##########################################
#
#       | Operator | Meaning           |
#       | -------- | ----------------- |
#       | `and`    | both must be True |
#       | `or`     | any one True      |
#       | `not`    | reverse           |
#
